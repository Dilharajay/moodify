"""
tests/test_color_extractor.py — Unit Tests for utils/color_extractor.py
========================================================================

Color extraction tests work on real (but synthetic) images generated by Pillow.
We create simple solid-colour and gradient images in memory rather than loading
files from disk. This makes the tests:
    - Fast (no disk I/O)
    - Deterministic (same image every run)
    - Self-contained (no test fixture files to manage)

Key things we verify:
    - The palette output format (HEX strings, correct count)
    - The histogram output format (48 floats, correct range)
    - Correctness of dominant color detection (a solid red image should give #FF...)
    - The batch processing function (process_all_pending) integrates correctly
"""

