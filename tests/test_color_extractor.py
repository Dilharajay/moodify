# ─── Helpers ──────────────────────────────────────────────────────────────────

def save_test_image(tmp_path, filename: str,
                    color: tuple = (26, 26, 46),
                    size: tuple = (300, 300)) -> str:
    """
    Create a solid-colour PNG test image and save it to tmp_path.
    Returns the absolute path to the saved file.
    """
    img = Image.new("RGB", size, color=color)
    path = str(tmp_path / filename)
    img.save(path, format="PNG")
    return path


def save_gradient_image(tmp_path, filename: str) -> str:
    """
    Create a simple horizontal gradient from dark blue to bright orange.
    Useful for testing that the palette picks up multiple distinct colors.
    """
    width, height = 300, 300
    img = Image.new("RGB", (width, height))
    pixels = img.load()
    for x in range(width):
        t = x / (width - 1)
        r = int(26  + t * (255 - 26))
        g = int(26  + t * (140 - 26))
        b = int(46  + t * (0   - 46))
        for y in range(height):
            pixels[x, y] = (r, g, b)
    path = str(tmp_path / filename)
    img.save(path, format="PNG")
    return path


